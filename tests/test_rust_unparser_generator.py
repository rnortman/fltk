def test_doc_to_rust_expr_text_is_escaped() -> None:
    """Text content is emitted through rust_str_lit (quoted + escaped).

    ``text`` is a free function in ``fltk_unparser_core`` (not an associated function on
    ``Doc``), so it is emitted crate-qualified -- mirroring the Python backend's
    module-qualified ``combinators.text(...)`` -- rather than as ``Doc::text(...)``,
    which would not resolve and fail to compile.
    """
    gen = _gen()
    assert gen._doc_to_rust_expr(comb.text("abc")) == 'fltk_unparser_core::text("abc")'
    # A backslash and an embedded quote must be escaped by the Rust string-literal emitter.
    assert gen._doc_to_rust_expr(comb.text(r'a\b"c')) == r'fltk_unparser_core::text("a\\b\"c")'


def test_doc_to_rust_expr_concat_recurses() -> None:
    """Concat emits a vec! of the recursively-converted children."""
    gen = _gen()
    doc = comb.concat([comb.text("a"), comb.NBSP, comb.text("b")])
    assert (
        gen._doc_to_rust_expr(doc)
        == 'fltk_unparser_core::concat(vec![fltk_unparser_core::text("a"), Doc::Nbsp, fltk_unparser_core::text("b")])'
    )


def test_doc_to_rust_expr_nested_concat() -> None:
    """A nested Concat that survives flattening still converts recursively.

    ``combinators.concat`` flattens, so build a raw ``Concat`` to force nesting and
    confirm the recursion descends into it.
    """
    gen = _gen()
    inner = comb.Concat((comb.text("a"), comb.text("b")))
    doc = comb.Concat((inner, comb.text("c")))
    assert gen._doc_to_rust_expr(doc) == (
        "fltk_unparser_core::concat(vec!["
        'fltk_unparser_core::concat(vec![fltk_unparser_core::text("a"), fltk_unparser_core::text("b")]), '
        'fltk_unparser_core::text("c")])'
    )


@pytest.mark.parametrize(
    "doc",
    [
        comb.group(comb.text("a")),
        comb.nest(2, comb.text("a")),
        comb.join([comb.text("a"), comb.text("b")], comb.text(",")),
        comb.comment("// x"),
    ],
)
def test_doc_to_rust_expr_rejects_unsupported(doc: comb.Doc) -> None:
    """Group/Nest/Join/Comment are rejected identically to the Python backend.

    The Python backend's _doc_to_combinator_expr raises ValueError on these; the Rust
    backend must too, so a `join ... by group(...)` config errors on both rather than
    silently diverging.
    """
    gen = _gen()
    with pytest.raises(ValueError, match="Unknown Doc type"):
        gen._doc_to_rust_expr(doc)


