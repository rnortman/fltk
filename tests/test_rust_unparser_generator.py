def _pymethod_body(src: str, signature_fragment: str) -> str:
    """Extract a single 8-space-indented ``#[pymethods]`` method body.

    ``_method_body`` only recognizes 4-space-indented rule methods; the PyO3 wrapper methods are
    nested two levels deep (8-space ``fn`` indent), so a method body ends at the first ``\\n``
    followed by exactly 8 spaces and ``}`` (the method's own closing brace -- inner ``else {``
    blocks close at 12+ spaces). ``signature_fragment`` must uniquely identify the method's
    signature line.
    """
    start = src.index(signature_fragment)
    end = src.index("\n        }\n", start) + len("\n        }\n")
    return src[start:end]


