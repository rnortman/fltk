def test_cli_format_config_unreadable_exits_cleanly(tmp_path: Path) -> None:
    """A --format-config path that exists but cannot be opened exits 1 with a CLI error, not a traceback.

    ``parse_format_config_file`` pre-checks existence (raising FileNotFoundError) but then opens
    the path bare, so a directory / unreadable file raises a non-FileNotFoundError OSError
    (IsADirectoryError / PermissionError).  The handler must catch OSError -- not only
    (ValueError, FileNotFoundError) -- so such a path yields the clean ``Error: …`` / exit 1
    every other failure path produces, rather than a raw Python traceback.
    """
    runner = CliRunner()
    grammar_file = tmp_path / "g.fltkg"
    grammar_file.write_text("r := a:/x/;\n")
    out_file = tmp_path / "out.rs"
    # Point --format-config at a directory => IsADirectoryError (an OSError, not FileNotFoundError).
    result = runner.invoke(
        app, ["gen-rust-unparser", str(grammar_file), str(out_file), "--format-config", str(tmp_path)]
    )
    assert result.exit_code == 1
    # A clean typer.Exit, not an uncaught OSError traceback.
    assert isinstance(result.exception, SystemExit)
    assert "Error:" in result.output


