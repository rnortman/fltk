_SIMPLE_GRAMMAR = 'greeting := "hello";'


def _gen() -> RustUnparserGenerator:
    return RustUnparserGenerator(parse_grammar(_SIMPLE_GRAMMAR))


def _method_body(src: str, fn_name: str) -> str:
    """Extract a single generated method's source (signature line through its body).

    Negative assertions (e.g. "no ``pos + 1``") must be scoped to the rule under test: every
    grammar carries a synthetic ``_trivia`` rule whose regex content term emits a full regex
    body (``node.children()``, ``child_tuple.0 != Some(...)``, ``span.text()``, ``pos + 1``),
    so a file-global ``not in src`` check would catch that unrelated rule.
    """
    marker = f"fn {fn_name}("
    start = src.rfind("\n", 0, src.index(marker)) + 1
    rest = src[start:]
    body_start = rest.find("\n") + 1
    end = len(rest)
    for boundary in ("\n    fn ", "\n    pub fn ", "\n}"):
        pos = rest.find(boundary, body_start)
        if pos != -1:
            end = min(end, pos)
    return rest[:end]


def test_generate_emits_header_and_struct() -> None:
    """A minimal grammar produces the unparser-core import, cst import, and struct."""
    grammar = parse_grammar(_SIMPLE_GRAMMAR)
    src = RustUnparserGenerator(grammar).generate()
    assert "use fltk_unparser_core::{" in src
    # The unparser-core import must name every symbol the generated bodies rely on;
    # a dropped/misspelled symbol would not surface until a later compile increment.
    for symbol in ("DocAccumulator", "UnparseResult", "RendererConfig", "Renderer", "resolve_spacing_specs"):
        assert symbol in src
    # The core walk uses DocAccumulator/UnparseResult unconditionally, but the pipeline
    # types are consumed only by the python_bindings module, so their import is gated behind the
    # `python` feature -- otherwise a python-off build flags them as unused imports.
    assert "use fltk_unparser_core::{DocAccumulator, UnparseResult};" in src
    assert (
        '#[cfg(feature = "python")]\nuse fltk_unparser_core::{Renderer, RendererConfig, resolve_spacing_specs};'
    ) in src
    # _SIMPLE_GRAMMAR (a single suppressed literal, no separators) emits no spacing/anchor Doc,
    # so the bare `Doc` type is never referenced and its import is omitted -- importing it
    # unconditionally would be an unused import under -D warnings.
    assert "use fltk_unparser_core::Doc;" not in src
    assert "use super::cst;" in src
    # Generated function paths use double-underscore names; the allow attribute must be present.
    assert "#![allow(non_snake_case)]" in src
    assert "pub struct Unparser;" in src
    assert "pub fn new(" in src
    # The default (no source_name) header omits the 'from <name>' clause.
    assert "//! Generated by fltk gen-rust-unparser. Do not edit." in src


def test_doc_import_omitted_when_no_doc_expr() -> None:
    """A grammar that emits no `Doc::` constructor omits the (otherwise-unused) `Doc` import.

    Importing `Doc` unconditionally fails clippy `unused_imports` under `-D warnings` for any
    separator-less / spacing-less grammar (parity with the parser backend gating its
    Regex/OnceLock import).  `r := a:/x/ . b:/y/;` has only NO_WS (`.`) separators and the
    default config, so it references no bare `Doc::` variant.
    """
    src = RustUnparserGenerator(parse_grammar("r := a:/x/ . b:/y/;")).generate()
    assert "Doc::" not in src
    assert "use fltk_unparser_core::Doc;" not in src
    # The always-used core types are still imported.
    assert "use fltk_unparser_core::{DocAccumulator, UnparseResult};" in src


def test_doc_import_present_when_doc_expr_emitted() -> None:
    """A grammar whose default spacing emits a `Doc::` constructor imports the bare `Doc` type.

    A WS-required separator (`:`) yields a default `Doc::Line` separator spacing, so the bare
    `Doc` type is referenced and its import must be present.
    """
    src = RustUnparserGenerator(parse_grammar('pair := a:"x" : b:"y";')).generate()
    assert "Doc::Line" in src
    assert "use fltk_unparser_core::Doc;" in src


