def test_empty_alternative_body_is_passthrough() -> None:
    """A zero-item alternative emits only the pass-through return, with no threading preamble.

    The degenerate empty-alternative branch in ``_gen_alternative_body`` diverges most from
    the normal path, so exercise it directly: it must not emit the ``let mut pos``/``let mut
    acc`` preamble that the item-threading path uses.
    """
    gen = _gen()
    # Args: method-name prefix (`unparse_{rule}` for a rule body), rule name, class name.
    body = gen._gen_alternative_body("unparse_r", "r", "R", 0, gsm.Items(items=[], sep_after=[]))
    # The zero-item body never reads the CST node, so its parameter is named `_node`
    # (unused-variable clean under -D warnings); guards the `"node" if alt.items else "_node"`
    # branch against being dropped or inverted.
    assert "fn unparse_r__alt0(&self, _node: &cst::R, pos: usize, acc: DocAccumulator)" in body
    assert "Some(UnparseResult::new(acc, pos))" in body
    assert "let mut pos = pos;" not in body
    assert "let mut acc = acc;" not in body


