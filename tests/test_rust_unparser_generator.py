def test_rule_start_and_end_anchors_emit_push_pop() -> None:
    """Rule-level group/nest anchors emit push_* on entry and the matching pop chain on success."""
    fmt_config = FormatterConfig()
    rule_config = RuleConfig()
    rule_config.anchor_configs["before:rule_start:"] = AnchorConfig(
        selector_type=ItemSelector.RULE_START,
        selector_value="",
        operations=[
            FormatOperation(OperationType.GROUP_BEGIN),
            FormatOperation(OperationType.NEST_BEGIN, indent=2),
        ],
    )
    rule_config.anchor_configs["after:rule_end:"] = AnchorConfig(
        selector_type=ItemSelector.RULE_END,
        selector_value="",
        operations=[
            FormatOperation(OperationType.NEST_END),
            FormatOperation(OperationType.GROUP_END),
        ],
    )
    fmt_config.rule_configs["greeting"] = rule_config

    src = RustUnparserGenerator(parse_grammar(_SIMPLE_GRAMMAR), formatter_config=fmt_config).generate()
    # RULE_START: push operations rebind acc in config order.
    assert "let acc = acc.push_group();" in src
    assert "let acc = acc.push_nest(2);" in src
    # RULE_END: the success path pops in config order and rebuilds the result.
    assert "let acc = r.accumulator.pop_nest().pop_group();" in src
    assert "return Some(UnparseResult::new(acc, r.new_pos));" in src


